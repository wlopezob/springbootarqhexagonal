package pe.edu.galaxy.appcomisaria.application.ports.out.distrito;

public class DistritoPortException extends Exception {

	private static final long serialVersionUID = -8061702186303990281L;

	public DistritoPortException() {
	}

	public DistritoPortException(String message) {
		super(message);
	}

	public DistritoPortException(Throwable cause) {
		super(cause);
	}

	public DistritoPortException(String message, Throwable cause) {
		super(message, cause);
	}

	public DistritoPortException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}
}
