package pe.edu.galaxy.appcomisaria.application.ports.out.provincia;

public class ProvinciaPortException extends Exception {

	private static final long serialVersionUID = -8061702186303990281L;

	public ProvinciaPortException() {
	}

	public ProvinciaPortException(String message) {
		super(message);
	}

	public ProvinciaPortException(Throwable cause) {
		super(cause);
	}

	public ProvinciaPortException(String message, Throwable cause) {
		super(message, cause);
	}

	public ProvinciaPortException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}
}
